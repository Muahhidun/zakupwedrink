import re

names = [
    "Манговое пюре 1.2кг*12шт",
    "Апельсиновый концентрат 2.6кг*8шт",
    "Стакан 500 (1000шт/кор)",
    "Ложка большая (черная) (2000шт/кор)",
    "Пакет 4 чашки (3000шт/кор)",
    "Кокосовое молоко 1л*12шт",
    "Фасоль 0.9кг*12шт",
    "Желе шары 1кг*20шт"
]

for name in names:
    clean = re.sub(r'\s+([\d.,]+[а-яА-Яa-zA-Z]+\*\d+[а-яА-Яa-zA-Z]+|\(\d+[а-яА-Яa-zA-Z]+/[а-яА-Яa-zA-Z]+\))$', '', name)
    print(f"{name} -> {clean}")
